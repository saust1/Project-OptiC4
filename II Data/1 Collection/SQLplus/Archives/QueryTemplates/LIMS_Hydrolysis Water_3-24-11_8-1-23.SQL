SELECT T24."TREND TIME", 
    T24."TREND RESULT" AS "Hydrolysis Water_ppmNa2O",
    T25."TREND RESULT" AS "Hydrolysis Water_%NH4OH"

FROM "AS74550-5ppmNa2O".1 AS T24,
    "AS74550-5%NH4OH".1 AS T25

WHERE T24."TREND TIME" BETWEEN '24-MAR-11' AND '1-AUG-23'
    AND (T25."TREND TIME" = T24."TREND TIME")